//this helper method returns the lowest cost node in the costs object.  
//to circumvent the need for this method, we can use a priority queue.  
//Rei, if you have used priority queues before I would love to learn about them 
const getLowestCost = (costs, visited) => {
    const nodes = Object.keys(costs) //gets an array of nodes in costs object
    
    const lowestCostNode = nodes.reduce((lowest, node) => {
        if (lowest === null && !visited.includes(node)) {
          lowest = node;
        }
        if (costs[node] < costs[lowest] && !visited.includes(node)) {
          lowest = node;
        }
        return lowest;
    }, null);
  
    return lowestCostNode
  };



// Main algorithm. Will return the minimum cost and path to reach Finish
const dijkstrasAlgo = (graph, start, finish) => {

    const nodes = Object.keys(graph)
    const costs = {} //this will keep track of lowest cost to reach each node from starating node
    //initialize distances to infinity for each node excet start node to zero 
    for(const node of nodes){
        if(node == start){
            costs[node] = 0 
        } else {
            costs[node] = Infinity
        }
        }
    
    // initialize a track path variable (keeps track of the path we're taking)
    const parentNodes = {finish: null};
    for (let child in graph[start]) {
        parentNodes[child] = start;
        costs[child] = graph[start][child]
    }

    
    // track nodes that have already been processed
    const visited = [start];
    
    // Set initial node 
    let node = start  
    

    while (node) {
        const nodeCost = costs[node]; //distance from parent node to child node
        const childNodes = graph[node]; //gets all the child nodes for the parent node     
        
        for (let child in childNodes) {
            //skip if child node has already been analyzed 
            if(visited.includes(child)){ 
                continue
            } 
            let childCost = childNodes[child]  
            let costFromStartToChild = nodeCost + childCost
        
            if (costs[child] > costFromStartToChild) {
            costs[child] = costFromStartToChild;
            parentNodes[child] = node;
            }
        }
        visited.push(node);
        node = getLowestCost(costs, visited);  //gets lowest cost node that hasn't been visited
    }


    const path = [finish];
    let parentNode = parentNodes[finish];

    while (parentNode) {
    path.push(parentNode);
    parentNode = parentNodes[parentNode];
    }
    path.reverse();
        
        
    return path;
};


